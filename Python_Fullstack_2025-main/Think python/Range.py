# it is a build in function in python which is used for printing sequences
# range (start , stop , step)
for i in range(2, 5):  # stop value is excluded , and by default , start value is 0
    print(i)
print("done")
for i in range(5):
    print(i)
print("done")
for i in range(1, 7, 2):
    print(i)
print("done")
for i in range(10, 0, -1):
    print(i)

